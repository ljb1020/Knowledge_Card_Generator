export function buildRepairPrompt(
  topic: string,
  stage1Draft: string,
  invalidStage2: string,
  validationErrors: string
): string {
  return [
    '请修复下面这个 JSON 文档，使它完全满足要求的结构和内容限制。',
    '只输出 JSON 对象，不要 Markdown，不要解释。',
    '保留原本的教学价值，且所有可见内容都必须使用简体中文。',
    '主题必须严格保持与指定知识点一致。',
    '结果要清晰、专业、实用、细节到位，并且让前端读者觉得值得收藏。',
    '',
    `目标知识点：${topic}`,
    '',
    '原始讲解草稿：',
    stage1Draft,
    '',
    '校验错误：',
    validationErrors,
    '',
    '待修复的 JSON 或非法文档：',
    invalidStage2,
    '',
    '必须满足的约束：',
    '- styleVersion 必须等于 "frontend-card-v1"',
    '- cards 数量必须是 4 到 8 张',
    '- 第一张必须是 cover，最后一张必须是 summary，中间只能是 bullet',
    '- cover.tag 必须等于 "前端知识点"',
    '- cover.title 最多 24 个中文字符',
    '- cover.subtitle 最多 40 个中文字符',
    '- bullet.title 最多 22 个中文字符',
    '- 每张 bullet 卡片必须有 2 到 4 条 bullet',
    '- 每条 bullet 最多 30 个中文字符',
    '- summary.title 最多 22 个中文字符',
    '- summary.summary 最多 80 个中文字符',
    '- summary.cta 最多 24 个中文字符',
    '- 不能出现 HTML、Markdown、emoji 或代码块',
  ].join('\n');
}
